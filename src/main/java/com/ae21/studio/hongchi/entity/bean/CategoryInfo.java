package com.ae21.studio.hongchi.entity.bean;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Alex
 */
@Entity
@Table(name = "category_info")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "CategoryInfo.findAll", query = "SELECT c FROM CategoryInfo c"),
    @NamedQuery(name = "CategoryInfo.findById", query = "SELECT c FROM CategoryInfo c WHERE c.id = :id"),
    @NamedQuery(name = "CategoryInfo.findByName", query = "SELECT c FROM CategoryInfo c WHERE c.name = :name"),
    @NamedQuery(name = "CategoryInfo.findByUuid", query = "SELECT c FROM CategoryInfo c WHERE c.uuid = :uuid")})
public class CategoryInfo implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 250)
    @Column(name = "name")
    private String name;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 250)
    @Column(name = "uuid")
    private String uuid;
    @Basic(optional = false)
    @NotNull
    @Lob
    @Size(min = 1, max = 65535)
    @Column(name = "desc")
    private String desc;
    @ManyToMany(mappedBy = "categoryInfoList")
    private List<ProductInfo> productInfoList;

    public CategoryInfo() {
    }

    public CategoryInfo(Integer id) {
        this.id = id;
    }

    public CategoryInfo(Integer id, String name, String uuid, String desc) {
        this.id = id;
        this.name = name;
        this.uuid = uuid;
        this.desc = desc;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    @XmlTransient
    public List<ProductInfo> getProductInfoList() {
        return productInfoList;
    }

    public void setProductInfoList(List<ProductInfo> productInfoList) {
        this.productInfoList = productInfoList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof CategoryInfo)) {
            return false;
        }
        CategoryInfo other = (CategoryInfo) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.ae21.studio.hongchi.entity.bean.CategoryInfo[ id=" + id + " ]";
    }
    
}
